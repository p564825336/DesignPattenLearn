package pattern.learn15.abstract_factory;

public class User {

	private int _id;
	private int _name;
	public int get_id() {
		return _id;
	}
	public void set_id(int _id) {
		this._id = _id;
	}
	public int get_name() {
		return _name;
	}
	public void set_name(int _name) {
		this._name = _name;
	}
}
